    global cnt_bad, cnt_good

    entry_4.delete(0, END)   # —É–¥–∞–ª—è–µ–º —Ç–µ–∫—Å—Ç –∏–∑ –ø–æ–ª—è –¥–ª—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞

