    num = int(entry_1.get())     # —Å—á–∏—Ç—ã–≤–∞–µ–º —Å –æ–∫–Ω–∞ —á–∏—Å–ª–æ-–æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏–µ
